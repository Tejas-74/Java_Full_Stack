package a2_Operators;

public class Arithmatic {
    public static void main(String[] args) {
        int a = 800;
        int b = 400;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        double x = 800;
        double y = 400;


        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

    }
}
