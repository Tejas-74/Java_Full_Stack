package a1_Basics;

public class arithmaticOperations {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        int a = 9, b = 8, c = 4;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        a = 10;
        System.out.println(a);
        b = c;
        System.out.println(b);
    }


}
